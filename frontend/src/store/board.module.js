export const board = {
    namespaced: true,
    state: initialState,

    actions: {
        movePiece({ commit, state }, piece) {
            let newBoard = state.board;
            newBoard[piece.y][piece.x] = {
                id: piece.id,
                letter: piece.letter
            };
            commit('pieceMovedSuccess', newBoard);
        },

        nextTurn({ commit }) {
            commit('nextTurnSuccess');
        },
    },

    mutations: {
        nextTurnSuccess(state) {
            // function to increment the turn by one, and to store a copy of the
            // previous turn's board
            state.previousBoard = state.board;
            state.turn += 1;
            state.placed = false;
        },

        pieceMovedSuccess(state, board) {
            state.board = board;
            state.placed = true;
        }
    },
}
